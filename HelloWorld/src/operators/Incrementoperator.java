package operators;

public class Incrementoperator {

	public static void main(String[] args) {
		int a=60;
		int b=70;
		System.out.println("The value of  a "+a);
		System.out.println("The value of b  "+b);
		
		System.out.println("The value of --a "+(--a));
		System.out.println("The value of  a "+a);
		System.out.println("The value of b-- "+(b--));
		System.out.println("The value of  b "+b);

	}

}
